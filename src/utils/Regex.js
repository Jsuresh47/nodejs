const Regex = {
    emptyField: /^\s*$/,
    validEmail: /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
    validUsername: /^\w+[\\]\w+/,
    validPassword: /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[~!@#$%^&*()\-_=+{};:,<.>])[A-Za-z\d~!@#$%^&*()\-_=+{};:,<.>]{6,20}$/,
    specialCharCheck: /^([^~+@!#$%`;:’”,.|\\/?><(){}[]*)$/,
    validStream: /^([^~+@!#$%`;:’”,.|\\/?><]*)$/,
    validUrl: /[-a-zA-Z0-9@:%_\+.~#?&//=]{2,256}\.[a-z]{2,4}\b(\/[-a-zA-Z0-9@:%_\+.~#?&//=]*)?/,
  };
  
export default Regex;
  
